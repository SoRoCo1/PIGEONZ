// Shared pixel art icon renderer
// 0 = transparent  1 = dark outline  2 = accent  3 = highlight

export type IconGrid = (0|1|2|3)[][];

export function PixelIcon({ grid, accent, size = 2.5 }: {
  grid: IconGrid;
  accent: string;
  size?: number;
}) {
  const dark = '#100c1c';
  const hi   = accent + 'bb';
  const map: (string|null)[] = [null, dark, accent, hi];
  const rows = grid.length;
  const cols = grid[0].length;
  return (
    <svg
      width={cols * size} height={rows * size}
      style={{ display: 'block', imageRendering: 'pixelated', shapeRendering: 'crispEdges' }}
    >
      {grid.flatMap((row, ri) =>
        row.map((cell, ci) => {
          const fill = map[cell];
          if (!fill) return null;
          return <rect key={`${ri}-${ci}`} x={ci*size} y={ri*size} width={size} height={size} fill={fill} />;
        })
      )}
    </svg>
  );
}

